def circumference(r):
  return (2*3.14)*r
def triangle_area(b,h):
  return 0.5*(b*h)
def cone_volume(r,h):
  return (3.14*(r*r))*(h/3)
def sphere_surface_area(r):
  return (4*3.14)*(r*r)
def sphere_volume(r):
  return (1.33*3.14)*(r**3)
def pyramid_volume(l,w,h):
  return (l*w*h)/3
def circle_area(r):
  return 3.14 * (r*r)
